function drawMap() {
     for (var i = 0; i < map[0].length; i++){
          for(var j = 0; j < map.length; j++){
               if (map[j][i] == 1){
                    ctx.fillStyle = "#000000";
                    ctx.fillRect((i)*25, (j)*25, 25, 25)
               }
               if (map[j][i] == 0){
                    ctx.fillStyle = "#FFFFFF";
                    ctx.fillRect((i)*25, (j)*25, 25, 25)
               }
               if (map[j][i] == 2){
                    ctx.fillStyle = "#F34949";
                    ctx.fillRect((i)*25, (j)*25, 25, 25)
               }
          }
     }
}

